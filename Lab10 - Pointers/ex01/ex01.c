#include <stdio.h>

int main(){
    printf("<< Pointers >>\n");

    int val[5] = {2,4,5,8,10};
    int *ponteiro2, *ponteiro4;
    ponteiro2=&val[2];
    ponteiro4=&val[4];

    printf("Digite o novo valor de 5: ");
    scanf("%d", ponteiro2);
    printf("o valor 5 agora eh %d\n", val[2]);

    // c
    printf("Digite o novo valor de 10: ");
    scanf("%d", ponteiro4);
    printf("o valor 10 agora eh %d\n", val[4]);

    //e
    printf("Mostrando vetor usando o ponteiro para o segundo: ");
    for (int *p=ponteiro2-2, i=0; i<5; i++, p++){
        printf("%d ", *p);
    }
    printf("\n\n");

    //f
    printf("Subtraindo 1 de cada elemento: ");
    for (int *p=ponteiro2-2, i=0; i<5; i++, p++){
        *p=(*p)-1;
        printf("%d ", *p);
    }
    printf("\n");

    return 0;
}

